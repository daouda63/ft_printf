#include "includes/ft_printf.h"

int	putstr(char *s)
{
	int		i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			i += ft_putchar(s[i]);
		}
	}
	else
	{
		i = write(1, "(null)", 6);
	}
	return (i);
}
